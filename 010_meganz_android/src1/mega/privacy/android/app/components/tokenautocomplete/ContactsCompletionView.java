public class ContactsCompletionView extends TokenCompleteTextView<ContactInfo> {

    public ContactsCompletionView(Context context) {
        super(context);
    }

    public ContactsCompletionView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public ContactsCompletionView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
    }

    @Override
    protected View getViewForObject(ContactInfo contact) {
        LayoutInflater l = (LayoutInflater)getContext().getSystemService(Activity.LAYOUT_INFLATER_SERVICE);
        TokenTextView token = (TokenTextView) l.inflate(R.layout.contact_token, (ViewGroup) getParent(), false);
        token.setText(contact.getName());
        return token;
    }

    @Override
    protected ContactInfo defaultObject(String completionText) {
        //Stupid simple example of guessing if we have an email or not
        int index = completionText.indexOf('@');
        if (index == -1) {
            return new ContactInfo(completionText, completionText.replace(" ", "") + "@mega.nz");
        } else {
            return new ContactInfo(completionText.substring(0, index), completionText);
        }
    }


}